#include <stdio.h>

int main(){

    /*
     * ๏ฟฝำผ๏ฟฝ๏ฟฝฬถ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝศท๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝะถฯถ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝอธ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤธ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮช0สฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
     * */
    int number; //๏ฟฝ๏ฟฝยผ๏ฟฝรป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ

    int positiveNumber = 0;//๏ฟฝ๏ฟฝยผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤธ๏ฟฝ๏ฟฝ๏ฟฝ
    int negativeNumber = 0;//๏ฟฝ๏ฟฝยผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤธ๏ฟฝ๏ฟฝ๏ฟฝ

    while(1){//for(;;){
        printf("๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝาป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ(๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ0สฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ):");
        scanf("%d",&number);

        if(number > 0){
            positiveNumber++;
        }else if(number < 0){
            negativeNumber++;
        }else{
            break;
        }
    }

    printf("๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤธ๏ฟฝ๏ฟฝ๏ฟฝฮช๏ฟฝ๏ฟฝ%d\n",positiveNumber);
    printf("๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤธ๏ฟฝ๏ฟฝ๏ฟฝฮช๏ฟฝ๏ฟฝ%d\n",negativeNumber);

    return 0;
}
