// 指针变量的定义

#include <stdio.h>

int main() {
    //定义指针变量
    int *p1;
    int *p2;
    int *p3;
    printf("p1 = %p, p2 = %p, p3 = %p\n", p1, p2, p3);
    //注意：指针变量中只能存放地址，不要将一个整数（或任何其它非地址类型的数据）赋给一个指针变量。
    int a = 10;
    //  p1 = a; //将a对应的数据10赋给指针变量p1是错误的。因为p1记录的变量或常量在内存中的地址，而非数据值。

    //同一行声明两个指针变量
    char *p1Char, *p2Char; //此时的两个变量都是指针变量
    //错误的：
    //    char *p3Char,p4Char; //此时的p3Char是指针变量，p4Char是char类型变量

    // 1. 【配钥匙】：定义指针变量 p，并把变量 a 的地址赋值给它
    // * 在这里是身份标识，说明 p 是指针类型
    int *p = &a;

    // 2. 【看房卡】：直接打印 p 的值，显示的是它存的“房号”（即 a 的内存地址）
    // %p 是专门用于打印地址（十六进制）的占位符
    printf("p = %p\n", p);     // 输出：000000a2c4dffb5c

    // 3. 【进房间】：使用 *p 解引用，意思是“顺着 p 存的地址找过去，取出的值”
    // 此时 *p 就等价于 a
    printf("a = %d\n", *p);    // 输出：10

    // 4. 【对暗号】：验证 a 的地址和 p 存的值是否完全一致
    printf("&a = %p\n", &a);   // 输出：000000a2c4dffb5c

    // 比较的是两个“地址数值”是否相等
    if (p == &a) {
        printf("地址相同\n");
    }

    // 5. 【远程操控】：通过指针修改它所指向的那个内存空间里的数据
    // 这里的 * 是动作指令，表示“对地址指向的内容进行操作”
    *p = 200;

    // 验证修改结果：你会发现，改了 *p，原本的变量 a 也跟着变了
    printf("*p = %d\n", *p);   // 输出：200
    printf("a = %d\n", a);     // 输出：200（这证明了指针的威力）

    
    return 0;
}
